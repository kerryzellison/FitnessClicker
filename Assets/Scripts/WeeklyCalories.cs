using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class WeeklyCalories : MonoBehaviour
{
    public int DayCounter;
    public Text ThisWeekCaloriesText;
    int WeeklyCaloriesAmount;
    public int SavedWeeklyCalories;
    public Calories calories;
    void Update()
    {
        WeeklyCaloriesAmount = calories.calories + SavedWeeklyCalories;
        ThisWeekCaloriesText.text = "Calories Burned This Week: " + WeeklyCaloriesAmount;
        OnWeekEnd();
        

    }

    public void OnWeekEnd()
    {
        if (DayCounter > 7)
        {
            DayCounter = 1;
            SavedWeeklyCalories = 0;

        }
    }
    
    
}
